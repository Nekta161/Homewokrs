# Шаг 1: Запрос имени студента
name = input("Введите имя студента: ")

# Шаг 2: Запрос оценки студента
grade_input = input("Введите оценку студента: ")

# Шаг 3: Проверка, является ли введенная оценка числом
if grade_input.isdigit():
    grade = int(grade_input)  # Преобразуем строку в целое число

    # Шаг 4: Определение уровня знаний в зависимости от оценки
    if 1 <= grade <= 3:
        level = "Начальный уровень"
    elif 4 <= grade <= 6:
        level = "Средний уровень"
    elif 7 <= grade <= 9:
        level = "Достаточный уровень"
    elif 10 <= grade <= 12:
        level = "Высокий уровень"
    else:
        level = "Некорректная оценка"

    # Шаг 5: Вывод результата
    print(f"Имя студента: {name}. Уровень: {level}")

else:
    print("Введено некорректное значение. Пожалуйста, введите число.")
